                                                 <h4>ประวัติความเป็นมา</h4>
                                                 
<b>อุทยานวิทยาศาสตร์ภาคเหนือ มหาวิทยาลัยราชภัฏอุตรดิตถ์</b> ได้ดำเนินการตั้งแต่ปี พ.ศ. 2555 – ปัจจุบัน โดยทำงานร่วมกับอุทยานวิทยาศาสตร์ภาคเหนือที่มี 7 มหาวิทยาลัยร่วมกัน เป็นหน่วยงานในการทำหน้าที่ให้บริการด้านการวิจัยพัฒนาและถ่ายทอดเทคโนโลยี ส่งเสริมผู้ประกอบการในการนำวิทยาศาสตร์ เทคโนโลยีและนวัตกรรม ไปใช้ในการแก้ไขปัญหาและพัฒนาขีดความสามารถให้มีศักยภาพด้านการแข่งขันทางธุรกิจทั้งในระดับประเทศและนานาชาติ โดยมีการบริหารจัดการให้เกิดการเชื่อมโยงหน่วยงานวิจัยเอกชน สถาบันวิจัยของรัฐ และมหาวิทยาลัยเข้าด้วยกัน เพื่อให้เกิดการเชื่อมโยงงานวิจัยกับความต้องการของตลาด ด้วยการถ่ายทอดแลกเปลี่ยนความรู้ เทคโนโลยีและการจับคู่ธุรกิจ นอกจากนี้ยังมีการส่งเสริมธุรกิจเทคโนโลยี โดยผ่านกระบวนการบ่มเพาะธุรกิจเทคโนโลยี (Technology Business Incubation) หรือกระบวนการจัดตั้งหน่วยงานใหม่แยกออกจากหน่วยงานเดิม (spin-off) หรือกระบวนการอื่น เพื่อทำให้เกิดธุรกิจซึ่งใช้นวัตกรรมเป็นฐานเพิ่มขึ้นในประเทศอย่างต่อเนื่อง<p><u>อุทยานวิทยาศาสตร์ภาคเหนือ มหาวิทยาลัยราชภัฏอุตรดิตถ์ </u>มุ่งเน้นให้เกิดการพัฒนาในภาคอุตสาหกรรมให้มีความพร้อมทั้งในด้านองค์ความรู้ อุปกรณ์และเครื่องมือ การบริหารงาน การตลาด ตลอดจนผู้เชี่ยวชาญอาจารย์นักวิจัย และยังมีบทบาทในการประสานความร่วมมือระหว่างหน่วยงานที่เกี่ยวข้องเพื่อตอบสนองความต้องการของผู้ประกอบการและวิสาหกิจขนาดกลางและขนาดย่อมได้อย่างมีประสิทธิภาพ นำไปสู่การพัฒนาเศรษฐกิจสังคมต่อไป<p>
<b><u>วัตถุประสงค์</u></b><br>
1.  เพื่อสนับสนุนองค์ความรู้ด้านการวิจัยพัฒนาและถ่ายทอดเทคโนโลยี (Technology transfer) รวมทั้งการส่งเสริมให้เกิดธุรกิจที่ใช้เทคโนโลยี เพื่อเพิ่มมูลค่า (value add) ให้กับสินค้าและบริการ<br>
2.  เพื่อส่งเสริมผู้ประกอบการในการนำวิทยาศาสตร์ เทคโนโลยีและนวัตกรรม ไปใช้ในการแก้ไขปัญหาและพัฒนาขีดความสามารถเพิ่มโอกาสการแข่งขันทางธุรกิจ<br>
3.  เพื่อพัฒนาและส่งเสริมศักยภาพของผู้ประกอบการ ให้มีความเข้มแข็งสามารถพัฒนาได้
อย่างยั่งยืน <br>
4.  เพื่อเป็นแหล่งพัฒนาผลงานวิจัยสู่เชิงพาณิชย์ (Technology commercialization) รวมถึงพัฒนาเทคโนโลยีให้สามารถต่อยอดเพิ่มเติมในเชิงธุรกิจได้
